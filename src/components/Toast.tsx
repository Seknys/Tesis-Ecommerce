import React from "react";
import { ToastContainer, toast, Zoom } from "react-toastify";

export const SuccesToast = (message: string) => {
  toast.success(`${message}`, {
    position: "top-center",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "dark",
  });
};

